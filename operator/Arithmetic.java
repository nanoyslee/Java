package myprojects.learningjava.operator;

public class Arithmetic {

	public static void main(String[] args) {
		int result = 1 + 2;			// 3
        System.out.println(result);	
  
        result = result - 1;		// 2
        System.out.println(result); 
  
        result = result * 2;		// 4
        System.out.println(result); 
  
        result = result / 2;		// 2
        System.out.println(result); 
  
        result = result + 8;		// 10
        result = result % 7;		// 3  remainder
        System.out.println(result);

	}

}
